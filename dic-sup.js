var isDivVisible = false;
var div1 = document.getElementById("dic-m1");
var button1 = document.getElementById("dic-btn1");

button1.addEventListener("click", function(){
if(isDivVisible){
div1.style.display = "none";
isDivVisible = true;
}
else{
$("#dic-m1").toggle('fast');
div1.style.display = "block";
isDivVisible = false;
}
});

